package com.amalitechFiifi.chapter2;

public class ModifyAssignOperator {
    public static void main(String[] args){

        int xx =1;
        xx += 3; // x = x + 3;

        System.out.println(xx);

        int gpa = 10;
        gpa -= 0.5; // gpa = gpa - 0.5;
        System.out.println(gpa);

    }
}
