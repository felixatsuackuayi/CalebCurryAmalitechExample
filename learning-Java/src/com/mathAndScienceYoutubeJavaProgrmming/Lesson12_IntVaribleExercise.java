package com.mathAndScienceYoutubeJavaProgrmming;

public class Lesson12_IntVaribleExercise {

    /*
    *  Write a program that declares three variables named age1, age2, age3,
    * Assign any age you wish to each of the three variables,then use the print() method to output the ages to the screen separated by commas.
    *
    * */

    public static void main(String[] args){
        int age1;
        int age2;  //Here we declare the variables to hold ages
        int age3;

        age1=10;
        age2=14;   //Here we assign the ages to our variables
        age3=36;

        System.out.println(age1 + " " + age2 + " " + age3);

    }
}
