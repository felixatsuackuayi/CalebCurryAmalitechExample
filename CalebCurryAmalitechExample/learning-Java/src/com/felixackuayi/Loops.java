package com.felixackuayi;

public class Loops {

    public static void main(String args[]){

        int number = 0;
        number++;

        //same as writing

        int number1 = 0;

        number1 = number1 +1;

        System.out.println(number);
        System.out.println(number1);

        //if you want to increase a number by two

        int number2 = 0;

        number2 = number2 + 2;

        System.out.println(number2);

        int num3 = 1;
        num3 += 2;
        System.out.println(num3);

        int num4 = 1;//above can be written as this
        num4 = num4 + 2;
        System.out.println(num4);
    }
}
